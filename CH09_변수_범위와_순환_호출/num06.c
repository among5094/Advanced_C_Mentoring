#include <stdio.h>
// 9장 연습문제 6번

// 함수 선언
int sum(int n);

// 메인 함수
int main() {
    int n;

    printf("정수를 입력하십시오: ");
    scanf("%d", &n);

    printf("1부터 %d까지의 합 = %d\n", n, sum(n));

    return 0;
}

// sum 함수 구현 (재귀 함수)
int sum(int n) {
    if (n == 1) {
        return 1; // 더 이상 재귀 호출을 하지 않고 1을 반환
    } else {
        return n + sum(n - 1); // 재귀 호출: n + sum(n-1)
        // 재귀 호출: n이 1보다 크면, n을 더한 다음 sum(n-1)을 호출하여 계속해서 더함
        // 예를 들어, sum(5)를 호출하면, 5 + sum(4)를 계산함
        // sum(4)는 다시 4 + sum(3)으로 계산됨. 이 과정은 n이 1이 될 때까지 계속됨.
    }
}

/*
재귀 호출은 "함수가 자기 자신을 다시 호출하는 것"
이때, 함수는 문제를 더 작은 문제로 쪼개어 해결해 나감.
이 과정은 더 이상 쪼갤 수 없을 때까지 계속됨.
*/